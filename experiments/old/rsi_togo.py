def make_priors():
    """Extract core priors from Togo rule config."""
    return {rid: {'alpha': cfg['alpha'], 'beta': cfg['beta'],
                  'sigma_drift': cfg['sigma_drift']}
            for rid, cfg in TOGO_RULES.items()}


