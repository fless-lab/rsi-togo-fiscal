def load_dataset(path=CSV_PATH):
    """Load the pre-generated CSV."""
    if not os.path.exists(path):
        print(f"ERROR: {path} not found. Run generate_dataset.py first.")
        sys.exit(1)
    df = pd.read_csv(path)
    
    # Recover c_true from period 1 data
    dp = df[df['period'] == 1]
    c_true = {}
    for rid in RULE_IDS:
        vals = dp[dp[f'app_{rid}'] == True][f'c_true_{rid}'].dropna()
        if len(vals) > 0:
            # c_true is the population-level parameter, approximate from entity means
            # More precisely: the dataset was generated with a single c_true per rule
            # We estimate it as the mean of entity c_true values
            c_true[rid] = vals.mean()
        else:
            c_true[rid] = TOGO_RULES[rid]['alpha'] / (TOGO_RULES[rid]['alpha'] + TOGO_RULES[rid]['beta'])
    
    return df, c_true


def extract(df, period=1):
    """Extract signals, applicability, labels from dataframe."""
    dp = df[df['period'] == period].copy()
    
    sigs_disc = {r: {} for r in RULE_IDS}
    sigs_raw = {r: {} for r in RULE_IDS}
    apps = {r: {} for r in RULE_IDS}
    labels_global = {}
    labels_rule = {r: {} for r in RULE_IDS}
    ct_rule = {}
    
    for _, row in dp.iterrows():
        eid = row['entity_id']
        labels_global[eid] = int(row.get('label_any_nc', 0))
        
        for rid in RULE_IDS:
            app = bool(row.get(f'app_{rid}', False))
            apps[rid][eid] = app
            if app:
                sd = row.get(f'sig_disc_{rid}', np.nan)
                try: sd = float(sd)
                except: sd = np.nan
                sigs_disc[rid][eid] = sd
                
                sr = row.get(f'sig_raw_{rid}', np.nan)
                try: sr = float(sr)
                except: sr = np.nan
                sigs_raw[rid][eid] = sr
                
                lbl = int(row.get(f'label_{rid}', 0))
                labels_rule[rid][eid] = lbl
                
                ct = row.get(f'c_true_{rid}', np.nan)
                try: ct = float(ct)
                except: ct = np.nan
                ct_rule[(eid, rid)] = ct
    
    entity_ids = sorted(labels_global.keys())
    
    return {
        'sigs_disc': sigs_disc, 'sigs_raw': sigs_raw,
        'apps': apps, 'labels_global': labels_global,
        'labels_rule': labels_rule, 'ct_rule': ct_rule,
        'df': dp, 'entity_ids': entity_ids,
    }


def best_f1(yt, ys):
    bf, bt = 0, 0.5
    for tau in np.linspace(0.05, 0.95, 181):
        f = f1_score(yt, (ys >= tau).astype(int), zero_division=0)
        if f > bf:
            bf, bt = f, tau
    return bf, bt


