def exp1_per_rule(data, c_true):
    """EXP-1: Per-rule F1/AUC + Population calibration."""
    print("\n" + "="*70)
    print("EXP-1: Per-Rule Performance + Population Calibration")
    print("="*70)
    
    priors = make_priors()
    eids = data['entity_ids']
    
    # Population inference
    rsi = PopulationRSI(priors)
    pop_res = rsi.fit(data['sigs_disc'], data['apps'])
    
    # Entity scoring
    scorer = EntityScorer(RULE_IDS)
    ent_res = scorer.score(data['sigs_raw'], data['apps'], eids)
    
    # Per-rule metrics
    print(f"\n  Per-Rule Metrics:")
    print(f"  {'Rule':<10} {'n':>5} {'NC%':>5} {'RSI F1':>7} {'AUC':>7} {'RBS F1':>7} {'Δ':>6}")
    print("  " + "-"*55)
    
    per_rule = {}
    all_f1_rsi, all_f1_rbs, all_auc = [], [], []
    
    for rid in RULE_IDS:
        ae = [e for e in eids if data['apps'][rid].get(e, False)]
        if len(ae) < 10:
            continue
        yt = np.array([data['labels_rule'][rid].get(e, 0) for e in ae])
        if len(np.unique(yt)) < 2:
            print(f"  {rid:<10} {len(ae):>5} {yt.mean():>5.0%} {'skip (no var)':>30}")
            continue
        
        ys = np.array([ent_res['entity_rules'].get((e, rid), {}).get('nc_score', 0.5) for e in ae])
        f1_r, tau_r = best_f1(yt, ys)
        auc_r = roc_auc_score(yt, ys)
        
        yp_rbs = np.array([1 if (not np.isnan(data['sigs_disc'][rid].get(e, np.nan))
                                  and data['sigs_disc'][rid].get(e, np.nan) == 0) else 0 for e in ae])
        f1_rbs = f1_score(yt, yp_rbs, zero_division=0)
        
        d = f1_r - f1_rbs
        per_rule[rid] = {'f1_rsi': f1_r, 'auc': auc_r, 'f1_rbs': f1_rbs, 'n': len(ae), 'nc': yt.mean()}
        all_f1_rsi.append(f1_r); all_f1_rbs.append(f1_rbs); all_auc.append(auc_r)
        
        print(f"  {rid:<10} {len(ae):>5} {yt.mean():>5.0%} {f1_r:>7.3f} {auc_r:>7.3f} {f1_rbs:>7.3f} {d:>+6.3f}")
    
    mf_rsi = np.mean(all_f1_rsi); mf_rbs = np.mean(all_f1_rbs); ma = np.mean(all_auc)
    print(f"\n  Mean: RSI F1={mf_rsi:.3f} AUC={ma:.3f} | RBS F1={mf_rbs:.3f} | Δ={mf_rsi-mf_rbs:+.3f}")
    
    # Global aggregation comparison
    print(f"\n  Global (entity-level, secondary metric):")
    yt_g = np.array([data['labels_global'][e] for e in eids])
    ys_min = np.array([ent_res['entities'][e]['nc_min'] for e in eids])
    f1_min, _ = best_f1(yt_g, ys_min)
    auc_min = roc_auc_score(yt_g, ys_min) if len(np.unique(yt_g)) > 1 else 0
    
    yp_rbs_g = np.array([1 if any(not np.isnan(data['sigs_disc'][r].get(e, np.nan))
                                   and data['sigs_disc'][r].get(e, np.nan) == 0
                                   for r in RULE_IDS if data['apps'][r].get(e, False))
                         else 0 for e in eids])
    f1_rbs_g = f1_score(yt_g, yp_rbs_g, zero_division=0)
    
    # XGBoost
    dp = data['df']
    feat = ['obs_ca'] + [f'app_{r}' for r in RULE_IDS] + [f'sig_raw_{r}' for r in RULE_IDS]
    X = dp[feat].copy()
    for c in X.columns: X[c] = pd.to_numeric(X[c], errors='coerce')
    X = X.fillna(0)
    Xtr, Xte, ytr, yte = train_test_split(X, yt_g, test_size=0.3, random_state=42,
                                            stratify=yt_g if len(np.unique(yt_g)) > 1 else None)
    sc = StandardScaler(); Xtr_s = sc.fit_transform(Xtr); Xte_s = sc.transform(Xte)
    
    t0 = time.perf_counter()
    xgb = GradientBoostingClassifier(n_estimators=150, max_depth=4, random_state=42)
    xgb.fit(Xtr, ytr); xgb_ms = (time.perf_counter() - t0) * 1000
    f1_xgb = f1_score(yte, xgb.predict(Xte), zero_division=0)
    auc_xgb = roc_auc_score(yte, xgb.predict_proba(Xte)[:, 1]) if len(np.unique(yte)) > 1 else 0
    
    t0 = time.perf_counter()
    mlp = MLPClassifier(hidden_layer_sizes=(128, 64, 32), max_iter=1000,
                        random_state=42, early_stopping=True, learning_rate='adaptive')
    mlp.fit(Xtr_s, ytr); mlp_ms = (time.perf_counter() - t0) * 1000
    f1_mlp = f1_score(yte, mlp.predict(Xte_s), zero_division=0)
    auc_mlp = roc_auc_score(yte, mlp.predict_proba(Xte_s)[:, 1]) if len(np.unique(yte)) > 1 else 0
    
    print(f"  RSI (min-agg): F1={f1_min:.3f} AUC={auc_min:.3f} (no labels)")
    print(f"  RBS:           F1={f1_rbs_g:.3f} (no labels)")
    print(f"  XGBoost†:      F1={f1_xgb:.3f} AUC={auc_xgb:.3f} (supervised, {xgb_ms:.0f}ms)")
    print(f"  MLP†:          F1={f1_mlp:.3f} AUC={auc_mlp:.3f} (supervised, {mlp_ms:.0f}ms)")
    
    # Population calibration
    print(f"\n  Population Calibration:")
    print(f"  {'Rule':<10} {'c*':>7} {'E[c]':>7} {'σ':>7} {'CI 95%':>18} {'?':>3} {'n':>5}")
    n_ok = 0
    for rid in RULE_IDS:
        p = pop_res['population'][rid]; ct = c_true[rid]; ci = p['CI_95']
        ok = ci[0] <= ct <= ci[1]
        if ok: n_ok += 1
        print(f"  {rid:<10} {ct:>7.3f} {p['E_c']:>7.3f} {p['std_c']:>7.3f} [{ci[0]:.3f},{ci[1]:.3f}] {'✓' if ok else '✗':>2} {p['n_obs']:>5}")
    print(f"  Calibrated: {n_ok}/{len(RULE_IDS)}")
    
    # ELBO
    elbo = pop_res['elbo_history']
    print(f"\n  ELBO: prior={elbo[0]:.2f} → post={elbo[-1]:.2f} Gain={elbo[-1]-elbo[0]:+.2f}")
    
    return {
        'pop_res': pop_res, 'ent_res': ent_res, 'per_rule': per_rule,
        'xgb_ms': xgb_ms, 'mlp_ms': mlp_ms,
        'f1_xgb': f1_xgb, 'f1_mlp': f1_mlp,
    }


def exp2_t1(data):
    """EXP-2: O(1) Adaptability (T1)."""
    print("\n" + "="*70)
    print("EXP-2: O(1) Adaptability (T1)")
    print("="*70)
    
    priors = make_priors()
    rsi = PopulationRSI(priors)
    rsi.fit(data['sigs_disc'], data['apps'])
    
    pre = {k: rsi.pop['R1_TVA'][k] for k in ['mu_delta', 'sigma_delta', 'alpha_q', 'beta_q']}
    t_rsi = rsi.regulatory_update('R1_TVA', 100e6, 60e6)
    post = {k: rsi.pop['R1_TVA'][k] for k in ['mu_delta', 'sigma_delta', 'alpha_q', 'beta_q']}
    
    print(f"\n  VAT: 60M → 100M FCFA")
    print(f"  RSI: {t_rsi:.4f} ms")
    print(f"  σ_δ invariant: {abs(pre['sigma_delta'] - post['sigma_delta']) < 1e-12}")
    print(f"  α_q invariant: {abs(pre['alpha_q'] - post['alpha_q']) < 1e-12}")
    print(f"  β_q invariant: {abs(pre['beta_q'] - post['beta_q']) < 1e-12}")
    
    # XGBoost retrain time (from exp1)
    dp = data['df']; feat = ['obs_ca'] + [f'app_{r}' for r in RULE_IDS] + [f'sig_raw_{r}' for r in RULE_IDS]
    X = dp[feat].copy()
    for c in X.columns: X[c] = pd.to_numeric(X[c], errors='coerce')
    X = X.fillna(0); yt = np.array([data['labels_global'][e] for e in data['entity_ids']])
    Xtr, _, ytr, _ = train_test_split(X, yt, test_size=0.3, random_state=42,
                                       stratify=yt if len(np.unique(yt)) > 1 else None)
    t0 = time.perf_counter()
    GradientBoostingClassifier(n_estimators=150, max_depth=4, random_state=42).fit(Xtr, ytr)
    t_xgb = (time.perf_counter() - t0) * 1000
    
    sp = t_xgb / max(t_rsi, 0.0001)
    print(f"  XGBoost retrain: {t_xgb:.0f} ms → Speedup: {sp:,.0f}×")
    return {'rsi_ms': t_rsi, 'xgb_ms': t_xgb, 'speedup': sp}


